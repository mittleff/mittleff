@pytest.mark.parametrize("α, β, z", [
    (0.5, 1.0, -7.33057219000000e-02-5.11934762000000e-01j),
    (0.5, 1.0, +1.00809273000000e+01+2.22251668000000e+00j),
    (0.5, 1.0, -8.81638303000000e+00+4.53794350000000e+00j),
    (0.5, 1.0, -3.22342758000000e-01+8.45119872000000e+00j),
    (0.5, 1.0, -3.75588680000000e-01-9.83203507000000e+00j),
    (0.5, 1.0, +4.08373780000000e+00+2.53485316000000e+00j),
    (0.5, 1.0, -5.00775165000000e+00+4.08876443000000e+00j),
])
def test_erfc(α, β, z):
    '''
    $E_{1/2, 1}(z) = e^{z^2}\mathrm{erfc}(-z)$
    '''
    α, β, z = arb(α), arb(β), acb(z)
    expected = complex(acb.exp(z**2) * acb.erfc(-z))
    computed = mittleff(α, β, z, ε)
    assert(isclose(expected, computed))

